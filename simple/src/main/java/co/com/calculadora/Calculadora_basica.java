package co.com.calculadora;

public class Calculadora_basica {
	/**
	 * Realiza calculos basicos!
	 *
	 */
	public class Calculadora 
	{
	    public Integer suma( Integer num1, Integer num2 )
	    {
	        return num1 + num2;
	    }
	    public Integer resta( Integer num1, Integer num2 )
	    {
	        return num1 - num2;
	    }
	
	}
}
